import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "../..");

const envPath = path.join(repoRoot, ".env.local");
const identityMapPath = path.join(repoRoot, "backend/ai/identity/team-user-map.json");
const repoScopePath = path.join(repoRoot, "backend/ai/config/github-repo-scope.json");
const outputCsvPath = path.join(repoRoot, "backend/ai/generated/json_export_view.csv");
const outputSummaryPath = path.join(repoRoot, "backend/ai/generated/github-scope-summary.json");

const CSV_HEADERS = [
  "team_name",
  "quarter_label",
  "metric_name",
  "metric_value",
  "metric_unit",
  "source_system",
  "coverage_status",
  "note",
  "last_refresh_utc",
];

const AI_SIGNAL_PATTERNS = [
  /co-authored-by:.*copilot/i,
  /co-authored-by:.*\[bot\]/i,
  /co-authored-by:.*github/i,
  /\bcopilot\b/i,
  /\bai[- ]?(generated|assisted|created|authored)\b/i,
  /\bgenerated by\b/i,
  /\bcursor\b/i,
  /\bgithub copilot\b/i,
  /\bclaude\b/i,
  /\bchatgpt\b/i,
  /\baider\b/i,
];

const QUARTER_BOUNDARIES = {
  Q1: { start: "01-01", end: "03-31" },
  Q2: { start: "04-01", end: "06-30" },
  Q3: { start: "07-01", end: "09-30" },
  Q4: { start: "10-01", end: "12-31" },
};

const API_BASE = "https://api.github.com";

function parseEnv(text) {
  const env = {};

  for (const line of text.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const equalsIndex = trimmed.indexOf("=");
    if (equalsIndex === -1) continue;

    const key = trimmed.slice(0, equalsIndex).trim();
    const rawValue = trimmed.slice(equalsIndex + 1).trim();
    env[key] = rawValue.replace(/^['"]|['"]$/g, "");
  }

  return env;
}

function csvEscape(value) {
  const s = value === undefined || value === null ? "" : String(value);
  return /[",\r\n]/.test(s) ? `"${s.replace(/"/g, '""')}"` : s;
}

function toCsv(headersList, rows) {
  const lines = [headersList.join(",")];
  for (const row of rows) {
    lines.push(headersList.map((h) => csvEscape(row[h] ?? "")).join(","));
  }
  return `${lines.join("\n")}\n`;
}

function githubHeaders(token) {
  return {
    Accept: "application/vnd.github+json",
    Authorization: `Bearer ${token}`,
    "X-GitHub-Api-Version": "2022-11-28",
  };
}

async function fetchAllPages(url, token) {
  const results = [];
  let nextUrl = url;

  while (nextUrl) {
    const response = await fetch(nextUrl, { headers: githubHeaders(token) });

    if (!response.ok) {
      const body = await response.text();
      throw new Error(`GitHub API ${response.status} for ${nextUrl}: ${body}`);
    }

    const data = await response.json();
    results.push(...data);

    const linkHeader = response.headers.get("link") ?? "";
    const nextMatch = linkHeader.match(/<([^>]+)>;\s*rel="next"/);
    nextUrl = nextMatch ? nextMatch[1] : null;
  }

  return results;
}

function buildQuarterWindows() {
  const now = new Date();
  const year = now.getUTCFullYear();
  const currentQuarter = Math.floor(now.getUTCMonth() / 3) + 1;
  const windows = [];

  for (let q = 1; q <= currentQuarter; q++) {
    const key = `Q${q}`;
    const bounds = QUARTER_BOUNDARIES[key];
    windows.push({
      label: `${year}-${key}`,
      start: new Date(`${year}-${bounds.start}T00:00:00Z`),
      end: new Date(`${year}-${bounds.end}T23:59:59Z`),
    });
  }

  return windows;
}

function hasAiSignals(texts) {
  const combined = texts.join("\n");
  return AI_SIGNAL_PATTERNS.some((pattern) => pattern.test(combined));
}

function isUserActiveInPeriod(user, quarterStart, quarterEnd) {
  if (user.activeFrom) {
    const from = new Date(`${user.activeFrom}T00:00:00Z`);
    if (from > quarterEnd) return false;
  }

  if (user.activeTo) {
    const to = new Date(`${user.activeTo}T23:59:59Z`);
    if (to < quarterStart) return false;
  }

  return true;
}

async function fetchMergedPrs(repoFullName, since, until, token) {
  const sinceStr = since.toISOString().slice(0, 10);
  const prs = [];
  let page = 1;

  while (true) {
    const url = `${API_BASE}/repos/${repoFullName}/pulls?state=closed&sort=updated&direction=desc&per_page=100&page=${page}`;
    const response = await fetch(url, { headers: githubHeaders(token) });

    if (!response.ok) {
      const body = await response.text();
      console.error(`  Warning: failed to fetch PRs for ${repoFullName} page ${page}: ${response.status}`);
      break;
    }

    const batch = await response.json();

    if (batch.length === 0) break;

    let hasOlderThanWindow = false;

    for (const pr of batch) {
      if (!pr.merged_at) continue;

      const mergedAt = new Date(pr.merged_at);

      if (mergedAt < since) {
        hasOlderThanWindow = true;
        continue;
      }

      if (mergedAt <= until) {
        prs.push(pr);
      }
    }

    if (hasOlderThanWindow || batch.length < 100) break;

    page++;
  }

  return prs;
}

async function fetchPrCommitMessages(repoFullName, prNumber, token) {
  try {
    const url = `${API_BASE}/repos/${repoFullName}/pulls/${prNumber}/commits?per_page=100`;
    const commits = await fetchAllPages(url, token);
    return commits.map((c) => c.commit?.message ?? "");
  } catch {
    return [];
  }
}

async function main() {
  const now = new Date().toISOString();

  const envText = await fs.readFile(envPath, "utf8").catch(() => "");
  const env = parseEnv(envText);
  const identityMap = JSON.parse(await fs.readFile(identityMapPath, "utf8"));
  const repoScope = JSON.parse(await fs.readFile(repoScopePath, "utf8"));
  const users = Array.isArray(identityMap.users) ? identityMap.users : [];
  const repos = Array.isArray(repoScope.repos) ? repoScope.repos : [];

  const summary = {
    refreshedAt: now,
    source: "GitHub",
    usersTracked: users.length,
    reposConfigured: repos.length,
    status: "skipped",
    note: "",
    metrics: [],
  };

  if (repos.length === 0) {
    summary.note = "No repos configured in github-repo-scope.json. Run discover:github-repos first.";
    await writeOutputs([], summary);
    return;
  }

  if (!env.GITHUB_TOKEN) {
    throw new Error("Missing GITHUB_TOKEN in .env.local");
  }

  const loginToUser = new Map();
  for (const user of users) {
    const login = String(user.githubLogin ?? "").trim().toLowerCase();
    if (login) loginToUser.set(login, user);
  }

  const teamUsers = new Map();
  for (const user of users) {
    const team = String(user.team ?? "").trim();
    if (!team) continue;
    if (!teamUsers.has(team)) teamUsers.set(team, []);
    teamUsers.get(team).push(user);
  }

  const quarters = buildQuarterWindows();
  const csvRows = [];

  console.log(`Processing ${repos.length} repos across ${quarters.length} quarters for ${teamUsers.size} teams`);

  for (const quarter of quarters) {
    console.log(`\nQuarter: ${quarter.label}`);

    const teamPrData = new Map();
    for (const team of teamUsers.keys()) {
      teamPrData.set(team, { totalPrs: 0, aiPrs: 0, activeDevs: new Set(), aiDevs: new Set() });
    }

    for (const repoFullName of repos) {
      console.log(`  Scanning ${repoFullName}...`);

      let mergedPrs;
      try {
        mergedPrs = await fetchMergedPrs(repoFullName, quarter.start, quarter.end, env.GITHUB_TOKEN);
      } catch (error) {
        console.error(`  Failed to fetch PRs for ${repoFullName}: ${error.message}`);
        continue;
      }

      console.log(`    ${mergedPrs.length} merged PRs in window`);

      for (const pr of mergedPrs) {
        const authorLogin = String(pr.user?.login ?? "").toLowerCase();
        const user = loginToUser.get(authorLogin);
        if (!user) continue;

        const team = user.team;
        const data = teamPrData.get(team);
        if (!data) continue;

        if (!isUserActiveInPeriod(user, quarter.start, quarter.end)) continue;

        data.totalPrs++;
        data.activeDevs.add(authorLogin);

        const textsToScan = [pr.title ?? "", pr.body ?? ""];

        const commitMessages = await fetchPrCommitMessages(repoFullName, pr.number, env.GITHUB_TOKEN);
        textsToScan.push(...commitMessages);

        if (hasAiSignals(textsToScan)) {
          data.aiPrs++;
          data.aiDevs.add(authorLogin);
        }
      }
    }

    for (const [team, data] of teamPrData) {
      const activeMembers = teamUsers.get(team).filter((u) =>
        isUserActiveInPeriod(u, quarter.start, quarter.end),
      );

      const aiPrCoverage = data.totalPrs > 0 ? (data.aiPrs / data.totalPrs) * 100 : 0;
      const aiActiveDevelopers = data.aiDevs.size;

      console.log(`  ${team}: ${data.totalPrs} PRs, ${data.aiPrs} AI-assisted, ${aiActiveDevelopers} AI-active devs`);

      csvRows.push({
        team_name: team,
        quarter_label: quarter.label,
        metric_name: "AI-assisted Pull Request Coverage",
        metric_value: aiPrCoverage.toFixed(4),
        metric_unit: "percent",
        source_system: "GitHub",
        coverage_status: "automated",
        note: `${data.aiPrs} of ${data.totalPrs} merged PRs had AI signals`,
        last_refresh_utc: now,
      });

      csvRows.push({
        team_name: team,
        quarter_label: quarter.label,
        metric_name: "AI Active Developers",
        metric_value: String(aiActiveDevelopers),
        metric_unit: "count",
        source_system: "GitHub",
        coverage_status: "automated",
        note: `${aiActiveDevelopers} of ${activeMembers.length} team members`,
        last_refresh_utc: now,
      });

      summary.metrics.push({
        team,
        quarter: quarter.label,
        totalPrs: data.totalPrs,
        aiPrs: data.aiPrs,
        aiPrCoverage: Number(aiPrCoverage.toFixed(4)),
        activeDevs: data.activeDevs.size,
        aiActiveDevs: aiActiveDevelopers,
      });
    }
  }

  const eduRows = buildEduRollup(csvRows, quarters, now);
  csvRows.push(...eduRows);

  summary.status = "completed";
  summary.note = `Processed ${repos.length} repos, ${quarters.length} quarters, ${csvRows.length} metric rows generated.`;

  await writeOutputs(csvRows, summary);
}

function buildEduRollup(teamRows, quarters, now) {
  const eduRows = [];

  for (const quarter of quarters) {
    const quarterTeamRows = teamRows.filter((r) => r.quarter_label === quarter.label);

    const prCoverageRows = quarterTeamRows.filter((r) => r.metric_name === "AI-assisted Pull Request Coverage");
    const aiDevRows = quarterTeamRows.filter((r) => r.metric_name === "AI Active Developers");

    if (prCoverageRows.length > 0) {
      let totalAiPrs = 0;
      let totalPrs = 0;

      for (const row of prCoverageRows) {
        const match = String(row.note).match(/^(\d+) of (\d+)/);
        if (match) {
          totalAiPrs += Number(match[1]);
          totalPrs += Number(match[2]);
        }
      }

      const eduCoverage = totalPrs > 0 ? (totalAiPrs / totalPrs) * 100 : 0;

      eduRows.push({
        team_name: "EDU",
        quarter_label: quarter.label,
        metric_name: "AI-assisted Pull Request Coverage",
        metric_value: eduCoverage.toFixed(4),
        metric_unit: "percent",
        source_system: "GitHub",
        coverage_status: "automated",
        note: `${totalAiPrs} of ${totalPrs} merged PRs had AI signals (portfolio rollup)`,
        last_refresh_utc: now,
      });
    }

    if (aiDevRows.length > 0) {
      let totalAiDevs = 0;
      let totalMembers = 0;

      for (const row of aiDevRows) {
        const match = String(row.note).match(/^(\d+) of (\d+)/);
        if (match) {
          totalAiDevs += Number(match[1]);
          totalMembers += Number(match[2]);
        }
      }

      eduRows.push({
        team_name: "EDU",
        quarter_label: quarter.label,
        metric_name: "AI Active Developers",
        metric_value: String(totalAiDevs),
        metric_unit: "count",
        source_system: "GitHub",
        coverage_status: "automated",
        note: `${totalAiDevs} of ${totalMembers} members (portfolio rollup)`,
        last_refresh_utc: now,
      });
    }
  }

  return eduRows;
}

async function writeOutputs(csvRows, summary) {
  await fs.mkdir(path.dirname(outputCsvPath), { recursive: true });
  await Promise.all([
    fs.writeFile(outputCsvPath, toCsv(CSV_HEADERS, csvRows), "utf8"),
    fs.writeFile(outputSummaryPath, `${JSON.stringify(summary, null, 2)}\n`, "utf8"),
  ]);

  console.log(`\nWrote ${csvRows.length} rows to ${path.relative(repoRoot, outputCsvPath)}`);
  console.log(JSON.stringify(summary, null, 2));
}

main().catch((error) => {
  console.error(error.message);
  process.exitCode = 1;
});
